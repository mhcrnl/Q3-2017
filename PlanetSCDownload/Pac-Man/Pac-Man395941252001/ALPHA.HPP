class Alphabets{
	static PicturePtr alpha[26];
	static Picture other;
	static void initialize();
	static void deinitialize();
	static void displayText(int,int,const char*);
	friend class Font;
};
PicturePtr Alphabets::alpha[26] = {0};
Picture Alphabets::other(7,mju);
void Alphabets::initialize() {
	if(alpha[0] == 0) {
		for(int i=0;i<26;i++)
			alpha[i] = new Picture(7,alphabets[i]);
	}
}

void Alphabets::deinitialize() {
	if(alpha[0]) {
		for(int i=0;i<26;i++) {
			delete alpha[i];
			alpha[i] = 0;
		}
	}
}

void Alphabets::displayText(int x,int y,const char* string) {
	for(int i=0;string[i]!='\0';i++) {
		if(string[i] >='A' && string[i] <='Z')
			alpha[(string[i]-65)%26]->displayPicture(x+i,y);
		if(string[i] == 'æ')
			other.displayPicture(x+i,y);

	}
}
